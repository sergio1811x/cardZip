import { describe, expect, it } from "vitest";
import { validateWrittenDoc } from "./documentWriter";

describe("validateWrittenDoc — cargo structure", () => {
  it("rejects a cargo doc with raw sentences instead of structured request lists", () => {
    const bad = [
      "# ТЗ карго",
      "",
      "## Товар",
      "Название: Компактный фен",
      "Ссылка: https://example.com/item",
      "SKU: не определён",
      "Цена: 28 ¥",
      "",
      "## Что нужно запросить для доставки",
      "Уточнить рабочее напряжение и частоту питания.",
      "Уточнить стандарт вилки и наличие версии под нужный рынок.",
      "Запросить вес одной единицы с упаковкой.",
      "Запросить габариты индивидуальной упаковки.",
      "Запросить количество в мастер-коробе.",
      "",
      "## Дополнительно по этому товару",
      "Электрический прибор с мотором.",
      "",
      "## Текущий статус",
      "Вес: не указан",
      "Габариты: не указаны",
      "SKU: не определён",
      "",
      "## Важно",
      "Карго не рассчитывается точно без веса и габаритов выбранного SKU.",
    ].join("\n");

    expect(validateWrittenDoc(bad, "cargo")).toBe(false);
  });

  it("accepts a cargo doc with structured logistics lists", () => {
    const good = [
      "# ТЗ карго",
      "",
      "## Товар",
      "Название: Компактный фен",
      "Ссылка: https://example.com/item",
      "SKU: не определён",
      "Цена: 28 ¥",
      "",
      "## Что нужно запросить для доставки",
      "- Подтвердите рабочее напряжение и частоту питания по выбранному SKU.",
      "- Укажите стандарт вилки и наличие версии под нужный рынок.",
      "- Сообщите вес одной единицы с индивидуальной упаковкой.",
      "- Дайте габариты индивидуальной упаковки.",
      "- Укажите количество единиц в мастер-коробе.",
      "- Сообщите вес и габариты мастер-короба.",
      "- Пришлите фото индивидуальной упаковки и транспортной коробки.",
      "",
      "## Дополнительно по этому товару",
      "- Если кейс входит в SKU, нужны его размеры и защита от деформации при штабелировании.",
      "- Для электроприбора нужно подтвердить маркировку питания на товаре и упаковке.",
      "",
      "## Текущий статус",
      "Вес: не указан",
      "Габариты: не указаны",
      "SKU: не определён",
      "",
      "## Важно",
      "Карго не рассчитывается точно без веса и габаритов выбранного SKU.",
    ].join("\n");

    expect(validateWrittenDoc(good, "cargo")).toBe(true);
  });
});
