export function getCategoryRules(categoryType: ProductCategoryType): CategoryRule {
  return RULES[categoryType] ?? RULES.other;
}

export function isForbiddenField(categoryType: ProductCategoryType, fieldName: string): boolean {
  const rules = getCategoryRules(categoryType);
  const lower = fieldName.toLowerCase();
  return rules.forbiddenFields.some((f) => lower.includes(f.toLowerCase()));
}

export function detectCategoryFromAttributes(
  categoryName: string | undefined,
  attributes: Array<{ name: string; value: string }>,
  titleCn: string,
): ProductCategoryType {
  const hay = `${categoryName ?? ''} ${titleCn} ${attributes.map((a) => `${a.name} ${a.value}`).join(' ')}`.toLowerCase();

  if (/鞋|拖鞋|凉鞋|靴|sabo|crocs|шлёп|сабо|тапк|шлепан|сандал|кроссов|ботинк|туфл/.test(hay)) return 'shoes';
  if (/服|裤|衣|裙|dress|shirt|jacket|pants|clothing|apparel|брюк|платье|куртк|футболк|юбк|леггинс/.test(hay)) return 'clothes';
  if (/电|灯|充电|蓝牙|wifi|battery|lamp|projector|фен|проектор|лампа|зарядк|наушник/.test(hay)) return 'electronics';
  if (/刀|厨|锅|杯|碗|盘|knife|kitchen|нож|кухн|сковород|кастрюл|чайник/.test(hay)) return 'kitchen';
  if (/美容|护肤|化妆|крем|маск|шампун|косметик/.test(hay)) return 'beauty';
  if (/包|钱包|皮带|手表|眼镜|кошелёк|сумк|ремен|очк|часы/.test(hay)) return 'accessory';
  if (/家居|家用|home|декор|подушк|одеял|полотенц|штор/.test(hay)) return 'home';

  return 'other';
}
